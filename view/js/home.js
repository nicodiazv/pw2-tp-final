function volver() {
    $("#lblIniciarSesion").text('Inicia Sesión');
    $("#pnlSesion").show();
    $("#pnlRegistro").hide();
    $("#rickiSelañando").show();
}

function Registrarse() {
    // $("#lblIniciarSesion").text('Registrarse');
    $("#pnlRegistro").show();
    $("#pnlSesion").hide();
    $("#rickiSelañando").show();
}